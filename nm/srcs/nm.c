#include "ft_nm.h"

void			nm(t_env *e)
{
	e->magic_nb = *(int*)e->ptr;
	if (e->magic_nb == MH_MAGIC_64)
		handle_64(e);
	if (e->magic_nb == MH_MAGIC)
		handle_32(e);
}
